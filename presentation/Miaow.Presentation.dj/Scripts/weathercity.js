function showWeatherCity() {
    $("#wProvince").remove();
    var iptName = $(this).attr("id");
    var iptOffSet = $(this).offset();
    var iptLeft = iptOffSet.left - 268;
    var iptTop = iptOffSet.top + 30;
    var str = "<div id='wProvince' class='dialog'><span><em>请选择地点</em><a id='gb'></a><a id='dialogBack'>返回省份列表</a></span><ul></ul><div style='clear:both;'></div></div>";
    areasLen = weatherProvince.length;
    var str2 = "";
    for (var i = 0; i < areasLen; i++) {
        str2 = str2 + "<li id='wCode" + weatherProvince[i][1] + "'>" + weatherProvince[i][0] + "</li>";
    }

    $("body").append(str);
    $("#wProvince ul").append(str2);
    $("#wProvince").css({
        left: iptLeft + "px",
        top: iptTop + "px"
    });
    $("#wProvince span a#dialogBack").bind("click", function () {
        jQuery("#dialogBack").hide();
        $("#wProvince ul").empty();
        $("#wProvince ul").append(str2);
        $("#wProvince ul li").bind("click", {
            iptn: iptName
        }, CidyBind);
    });
    $("#wProvince span a#gb").bind("click", function () {
        $("#wProvince").remove();
    });
    $("#wProvince ul li").bind("click", {
        iptn: iptName
    }, CidyBind);
}
function setCity(event) {
    var setipt2 = event.data.ipt2;
    var iptText = $(this).attr("title");
    var iptVal = $(this).attr("id");
    if (iPowTour != null) {
        iPowTour.getWeather(iptVal, "weatherPanel");
        $("#areaWeather").text(iptText + "天气");
        $("#wProvince").remove();
    }
}
function CidyBind(event) {
    var setipt = event.data.iptn;
    var liId = $(this).attr("id");
    var liText = $(this).text();
    var pArr = eval(liId);
    pLen = pArr.length;
    if (pLen == 0) {
        $("#" + setipt + "Val").val(liId.substring(5, 9));
        $("#" + setipt).val(liText);
        $("#wProvince").css("display", "none");
    }
    else {
        listr = "";
        jQuery("#dialogBack").show();
        for (j = 0; j < pLen; j++) {
            listr = listr + "<li id='" + pArr[j][1] + "' title='" + pArr[j][0] + "'>" + pArr[j][0].substring(0, 3) + "</li>";
        }
        $("#wProvince ul").empty();
        $("#wProvince ul").append(listr);
        $("#wProvince ul li").bind("click", { ipt2: setipt }, setCity);
    }
}
