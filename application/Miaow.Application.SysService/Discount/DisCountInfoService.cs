    	    Miaow.Domain.Repository.IDisCountInfoRepository   disCountInfoRepository  ;
    
            public DisCountInfoService( Miaow.Domain.Repository.IDisCountInfoRepository disCountInfo)
            {
                if (disCountInfo == null)
                {
                    throw new ArgumentNullException(" disCountInfoRepository   is null");
                }
                disCountInfoRepository = disCountInfo;
            }
    
            public bool Add(Miaow.Infrastructure.Data.DataSys.Sys_DisCountInfo enitty, Miaow.Infrastructure.Data.DataSys.Sys_AdminUser operUser)
            {
                var res = false;
                if (enitty != null)
                {
                    try
                    {
                        disCountInfoRepository.Add(enitty);
                        disCountInfoRepository.Uow.Commit();
                        res = true;
                    }
                    catch (Exception ex)
                    {
                    }
                }
                return res;
            }
    
            public bool Add(IList<Miaow.Infrastructure.Data.DataSys.Sys_DisCountInfo> entity, Miaow.Infrastructure.Data.DataSys.Sys_AdminUser operUser)
            {
                var res = false;
                if (entity != null && entity.Count > 0)
                {
                    try
                    {
                        foreach (var item in entity)
                        {
                            if (item != null)
                            {
                                disCountInfoRepository.Add(item);
                            }
                        }
                        disCountInfoRepository.Uow.Commit();
                        res = true;
                    }
                    catch (Exception ex)
                    {
                    }
                }
                return res;
            }
    
     public bool Delete(Miaow.Infrastructure.Data.DataSys.Sys_DisCountInfo entity, Miaow.Infrastructure.Data.DataSys.Sys_AdminUser operUser)
